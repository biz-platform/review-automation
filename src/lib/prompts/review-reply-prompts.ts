/** 별점 문자열에서 숫자 추출 (예: "3점" → 3). 없으면 0(미지정) */
function parseStarRating(별점: string): number {
  const n = parseInt(별점.replace(/\D/g, ""), 10);
  return Number.isNaN(n) || n < 1 || n > 5 ? 0 : n;
}

