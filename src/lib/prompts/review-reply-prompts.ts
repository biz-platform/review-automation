/** 리뷰 본문이 없거나 별점만 있는 경우 등 — 프롬프트 분기용 */
export type ReviewBodyMode = "none" | "short" | "full";

const EMPTY_REVIEW_PLACEHOLDERS = new Set([
  "(내용 없음)",
  "(내용없음)",
  "(없음)",
  "(리뷰 없음)",
  "내용 없음",
]);

/**
 * 플랫폼에서 본문 없을 때 쓰는 플레이스홀더·짧은 감상만 있는 경우 등 구분
 */
export function classifyReviewBodyMode(리뷰_내용: string): ReviewBodyMode {
  const raw = 리뷰_내용.trim();
  if (!raw) return "none";
  const compact = raw.replace(/\s/g, "");
  if (EMPTY_REVIEW_PLACEHOLDERS.has(raw) || compact === "내용없음") {
    return "none";
  }
  // 짧은 한줄·이모지 위주 등 → full보다는 짧은 본문 톤
  if (raw.length < 45) return "short";
  return "full";
}

