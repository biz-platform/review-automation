/** 리뷰 본문이 없거나 별점만 있는 경우 등 — 프롬프트 분기용 */
export type ReviewBodyMode = "none" | "short" | "full";

const EMPTY_REVIEW_PLACEHOLDERS = new Set([
  "(내용 없음)",
  "(내용없음)",
  "(없음)",
  "(리뷰 없음)",
  "내용 없음",
]);

/**
 * 플랫폼에서 본문 없을 때 쓰는 플레이스홀더·짧은 감상만 있는 경우 등 구분
 */
export function classifyReviewBodyMode(리뷰_내용: string): ReviewBodyMode {
  const raw = 리뷰_내용.trim();
  if (!raw) return "none";
  const compact = raw.replace(/\s/g, "");
  if (EMPTY_REVIEW_PLACEHOLDERS.has(raw) || compact === "내용없음") {
    return "none";
  }
  // 짧은 한줄·이모지 위주 등 → full보다는 짧은 본문 톤
  if (raw.length < 45) return "short";
  return "full";
}

/**
 * Gemini user 턴: 시스템 프롬프트와 짝. 실제 서비스 초안·데모 API 공통.
 */
export function buildReviewReplyUserPrompt(
  리뷰_내용: string,
  commentLength: string,
): string {
  const mode = classifyReviewBodyMode(리뷰_내용);
  const len =
    COMMENT_LENGTH_INSTRUCTION[commentLength] ??
    COMMENT_LENGTH_INSTRUCTION.normal;

  const base = "위 지침에 따라 이 리뷰에 대한 댓글만 작성해 주세요.";

  if (mode === "none") {
    return `${base}
- 리뷰 본문이 없거나 별점만 있는 경우: 담백한 감사·인사만. 맛·메뉴·식감 등 구체 내용은 쓰지 말 것.
- 별점 숫자는 반복하지 말 것.
- ${len}
- 마크다운·서식 없이 평문만.`;
  }

  if (mode === "short") {
    return `${base}
- 짧은 본문이면 핵심만 짚고, 같은 말 반복·긴 인용 금지.
- 고객이 쓴 단어·문장을 그대로 따라 말하지 말고, 의미만 이해해 사장님 말투로 답할 것.
- 광고 문구·AI 자동답처럼 매끈한 문장만 나열하지 말고, **사람이 짧게 쓴 댓글**처럼 호흡을 섞을 것.
- 시스템에 지정된 **[톤 & 스타일]**(기본/2030 여·남/4050)을 그대로 유지할 것. 기본 말투로 축소하지 말 것.
- ${len}
- 마크다운·서식 없이 평문만.`;
  }

  return `${base}
- 감사·공감·마무리는 **고정 순서가 아니어도 됨.** 비슷한 문장 반복만 피할 것.
- 고객 표현을 에코하거나 형용사만 골라 나열하지 말고, 내용을 **재구성**해 자연스럽게 녹일 것.
- 시스템에 지정된 **[톤 & 스타일]**을 반드시 반영할 것. 모든 말투를 한 가지로 통일하지 말 것.
- 문장이 전부 **너무 매끈하거나 광고 카피 같으면** 안 됨. 짧은 문장·구어체 어미를 섞어도 됨(해요체 유지).
- ${len}
- 마크다운·서식 없이 평문만.`;
}

